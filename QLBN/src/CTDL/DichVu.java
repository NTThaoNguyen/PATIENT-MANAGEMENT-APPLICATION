package CTDL;

public class DichVu {
    String maDV;
    String tienDV;

    public DichVu() {
    }

    public DichVu(String maDV, String tienDV) {
        this.maDV = maDV;
        this.tienDV = tienDV;
    }

    public String getMaDV() {
        return maDV;
    }

    public String getTienDV() {
        return tienDV;
    }

    public void setMaDV(String maDV) {
        this.maDV = maDV;
    }

    public void setTienDV(String tienDV) {
        this.tienDV = tienDV;
    }
    
}
